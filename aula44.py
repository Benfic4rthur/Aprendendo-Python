# metodos uteis na lista
# append, insert, pop, del, clear, extend, +
lista = [10, 20, 30, 40, 50]  # lista original
print(lista)  # mostra a lista
lista.append(60)  # adiciona na ultima posicao
print(lista)  # mostra a lista com o elemento adicionado
ultimo_valor = lista.pop()  # remove o elemento da ultima posicao, o pop tambem salva o ultimo valor em uma variavel
print(lista, ' ultimo valor removido: ', ultimo_valor)  # mostra a lista com o elemento removido

