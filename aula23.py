# formatação f-strings
variavel = 'abc'
print(f'{variavel: >10}') # < alinhamento a esquerda e > alinhamento a direita sempre se coloca o caracter que deseja utilizad depois dos dois pontos
print(f'{variavel: <10}') # < alinhamento a esquerda e > alinhamento a direita sempre se coloca o caracter que deseja utilizad depois dos dois pontos
print(f'{variavel: ^10}') # ao centro
print(f'{1520.6564516566451121:.2f}')
print(f'{1520.6564516566451121:,.2f}')
print(f'{1520.6564516566451121:+,.2f}')